package model;

public class Service {
	Long servId;
	String servName;
	String servDescrip;
	String servMoney;
	Double servPrice;
	
	public Long getServId() {
		return servId;
	}
	public void setServId(Long servId) {
		this.servId = servId;
	}
	public String getServName() {
		return servName;
	}
	public void setServName(String servName) {
		this.servName = servName;
	}
	public String getServDescrip() {
		return servDescrip;
	}
	public void setServDescrip(String servDescrip) {
		this.servDescrip = servDescrip;
	}
	public String getServMoney() {
		return servMoney;
	}
	public void setServMoney(String servMoney) {
		this.servMoney = servMoney;
	}
	public Double getServPrice() {
		return servPrice;
	}
	public void setServPrice(Double servPrice) {
		this.servPrice = servPrice;
	}
}
